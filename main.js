function boolToWord( bool ){

  if (bool === true) return 'Yes'
  if (bool === false) return 'No'
  //...
}

// params: bool   // a boolean value
// return: str    // boolean converted to a string
// e.g. boolToWord(true)
// results in "Yes"


// conditional if bool is true return 'Yes', if it's false return 'No'


module.exports = boolToWord
